namespace Orbifold.Numerics
{
    /// <summary>
    /// Enumeration of supported force units.
    /// </summary>
    public enum ForceUnit
    {
        /// <summary>
        /// Newton "N"
        /// </summary>
        Newton,
        /// <summary>
        /// Dyne "dyn" or "dy"
        /// </summary>
        Dyne,
        /// <summary>
        /// Pound "lbf"
        /// </summary>
        Pound,
        /// <summary>
        /// Pond "pond"
        /// </summary>
        Pond
    }
}