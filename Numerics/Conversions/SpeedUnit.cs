namespace Orbifold.Numerics
{
    /// <summary>
    /// Enumeration of supported speed units.
    /// </summary>
    public enum SpeedUnit
    {
        /// <summary>
        /// The admirality knot "admkn"
        /// </summary>
        AdmiralityKnot,
        /// <summary>
        /// The knot "kn"
        /// </summary>
        Knot,
        /// <summary>
        /// Meters per hour "m/h" or "m/hr"
        /// </summary>
        MetersPerHour,
        /// <summary>
        /// Meters per second "m/s" or "m/sec"
        /// </summary>
        MetersPerSecond,
        /// <summary>
        /// Miles per hour "mph"
        /// </summary>
        MilesPerHour
    }
}